package Gun16;

public class _02_ForLoop {
    public static void main(String[] args) {
        // While döngüsü
        int i=0;
        while (i<5){
            System.out.println("i = " + i);
            i++;
        }

        // for döngüsü
        for (int j=0; j<5;j++){
            System.out.println("j = " + j);
        }
    }
}
