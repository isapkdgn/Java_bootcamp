package Mentoring_BurakTurkmen.While_DoWhile;

public class While_DoWhile4 {
    public static void main(String[] args) {

        int sayi=1;
        while (sayi<11){
            System.out.println("SayÄ±: " + sayi);
            sayi++;
        }

        int doSayi=1;
        do {
            System.out.println("doSayi = " + doSayi);
            doSayi++;
        }while (doSayi<11);

    }
}
