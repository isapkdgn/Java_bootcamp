package Mentoring_BurakTurkmen.While_DoWhile;

public class While_DoWhile3 {
    public static void main(String[] args) {

        // Do-while nedir

        // while döngüsü
        int i=10;

        while (i<5){
            System.out.println("Hello While");
        }

        //do-while
        int a=10;
        do {
            System.out.println("Hello Do-While");
        }while (a<5);
    }
}
