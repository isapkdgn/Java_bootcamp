package Mentoring_OmerGoker._08_Arrays;

public class Soru_3 {
    public static void main(String[] args) {
        // For Each döngüsü ile, içinde 3 adet hayvan ismi olan bir arrayin elemanlarını yazdırınız.
        String[] hayvanlar={"kedi","köpek","tavşan"};
        for (String str:hayvanlar) {
            System.out.println(str);
        }
    }
}
